package Exercise1;

public class ExerciseOne {

    public static void main (String arg[]) {

        //Numero entero
        int number1 = 12;
        System.out.println(number1);
        // Decimal doble
        double num2 = 12.14;
        System.out.println(num2);
        // Dato que solo recibe una respuesta de verdadero o falso
        boolean num = num2 == number1;
        System.out.println(num);
        // Cadena de texto
        String car = "Volvo";
        System.out.println(car);

        // Suma
        int sum = number1 + 20;
        System.out.println(sum);
        // Resta
        int rest = number1 - 10;
        System.out.println(rest);
        // Multiplicación
        double multiplicatory = number1 * 20;
        System.out.println(multiplicatory);
        // División
        int division = number1 / 2;
        System.out.println(division);
    }
}
